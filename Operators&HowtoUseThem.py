# Exponents
expo_1 = 2 ** 8
# 256

# MOD or Remainder
mod_1 = 22 % 8
# 6

# Int division
int_div = 22 // 8
# 2

# Regular Division
div = 22 / 8
# 2.75

# Multiplication
mult = 3 * 3
# 9

# Subtraction
sub = 5 - 2
# 3

# Addition
add = 2 + 2

print("Addition: ", add, "| Subtraction: ", sub, "| Multiplication: ", mult, "| Division: ", div, "| INTEGER division: ", int_div, "| Division with Remainder: ", mod_1, "| Exponent: ", expo_1)
